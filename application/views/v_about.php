			<h2>About Us</h2>
			<p align="justify">Desa Ngabab merupakan salah satu Desa yang berada di Kecamatan Pujon, Kabupaten Malang, Propinsi Jawa Timur. Dari segi topografi, Desa Ngabab berada di dataran tinggi dan berbukit, dengan ketinggian 1200 m di atas permukaan laut dengan kisaran suhu antara 8-18 Â°C, curah hujan 2,3 s/d 2400 mm/th dan terletak di sebelah Pasar Mantung, pusat transaksi sayur-mayur terbesar di wilayah Pujon. Desa Ngabab dilewati jalur utama lalu lintas darat yang menghubungkan antara kota Kediri dan kota Malang. Desa Ngabab berbatasan langsung dengan Desa Tulungrejo (sebelah barat), Desa Sukomulyo (sebelah selatan), Desa Tawang Sari (sebelah utara), dan Desa Ngroto (sebelah timur). Luas wilayah Desa Ngabab adalah 1244,93 Ha. yang digunakan sebagai hutan lindung, hutan produksi, ladang, pemukiman, perkebunan, dan fasilitas umum lainnya. Potensi yang paling menonjol dari Desa Ngabab adalah dari segi pertanian dan peternakan.</p>

			<p align="justify">Desa Ngabab merupakan salah satu daerah paling potensial dalam segi pertanian di Kabupaten Malang. Ditinjau dari segi ekonomis, daerah ini merupakan salah satu penghasil sayur terbesar di Kabupaten Malang. Sayur mayur hasil pertanian dari daerah ini antara lain adalah wortel, cabe, kacang panjang, kentang, kubis, sawi, tomat, dan lainnya. Selain sayur mayur, potensi pertanian yang lain dari daerah ini adalah berupa tanaman buah-buahan (jeruk dan apel), tanaman palawija (jagung), dan tanaman padi (padi sawah). Pertanian dari daerah ini begitu subur karena didukung oleh potensi irigasi yang dimiliki, yaitu memiliki satu sungai dengan 12 mata air dan juga lahan pertanian yang sangat luas (787 Ha). Oleh karena itu, kualitas hasil pertanian dari daerah ini begitu tinggi dan terjamin. Hasil pertanian dari daerah Desa Ngabab telah tersebar ke berbagai daerah, seperti Jakarta, Kalimantan, Surabaya, Tuban, Lamongan, Jombang, Mojokerto, Blitar, dan lain sebagainya.</p>

			<p align="justify">Selain dari segi pertanian, Desa Ngabab merupakan daerah yang sangat potensial dalam segi peternakan. Desa Ngabab adalah penghasil susu sapi terbesar yang ada di Kecamatan Pujon. Setiap hari, rata-rata produksi susu yang dihasilkan di Desa Ngabab adalah sebanyak 9000 liter (9 ton). Produksi susu sebanyak itu diperoleh dari 1900 ekor Sapi Perah yang dimiliki oleh para warga di Desa Ngabab. Hasil susu tersebut kemudian disetorkan ke KUD yang ada di Desa Ngabab setiap pagi dan sore, dan kemudian dipasok ke produsen susu bertaraf internasional, seperti Nestle dan Granfield. Kepada KUD, susu tersebut dijual Rp. 3000/liter. Jadi penghasilan dari Hasil Susu di Desa Ngabab adalah Rp. 27.000.000 untuk setiap harinya. Selain Sapi Perah, hewan ternak yang dimiliki oleh para warga di Desa Ngabab adalah Sapi Potong (12 ekor), Kambing (1500 ekor), Ayam Buras (3000 ekor), Itik (1000 ekor), Entok (200 ekor), dan Angsa (50 ekor).</p>

			<p align="justify">Jumlah penduduk yang ada di Desa Ngabab adalah berjumlah 7412 orang dengan rincian 3677 orang berjenis kelamin perempuan dan 3735 orang berjenis kelamin laki-laki. Desa Ngabab terbagi menjadi 3 dusun, yaitu Dusun Krajan, Dusun Lemburejo, dan Dusun Torongrejo. Jumlah keseluruhan kepala keluarga yang ada di Desa Ngabab adalah 2185 KK. Struktur mata pencaharian penduduk Desa Ngabab adalah Petani (3087 orang), Pekerja sektor jasa/perdagangan (65 orang), dan pekerja sektor industri (41 orang).</p>
